package initialisation;

public class Moyenne {

	public static void main(String[] args) {
		// Declaration des valeurs
		float a;
		float b;
		float c;
		float d;
		float e;
		float M;
		
		//Initialisation des valeurs
		a=10;
		b=21;
		c=4;
		d=11;
		e=82;
		
		//Action
		M=a+b+c+d+e/5;
		
		//Affichage résultat
		System.out.println("la Moyenne de "+a + "; "+b + ";" +c+ "; "+d+"; "+e+ " est égale à "+M);

	}

}
